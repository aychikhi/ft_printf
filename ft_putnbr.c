#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	c;

	c = 0;
	if (n == -2147483648)
	{
		c += ft_putchar('-');
		c += ft_putchar('2');
		n = 147483648;
	}
	if (n < 0)
	{
		c += ft_putchar('-');
		n *= -1;
	}
	if (n > 9)
		c += ft_putnbr(n / 10);
	c += ft_putchar(((n % 10) + 48));
	return (c);
}
